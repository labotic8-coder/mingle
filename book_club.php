<p>All you have to do is take a photo of yourself, and at least 5 other book club members holding up a copy of my book and email it to&nbsp;<a href="mailto:readytomingle@minglecity.com">readytomingle@minglecity.com</a>. The photo can be with the Book Club all together or individually. Just make sure to send them all at once.&nbsp;</p>



