<h2 class="wp-block-heading">Michael Baisden</h2>



<p>Michael Baisden is one of the most influential radio personalities in history! At the peak of his career he could be heard by over 8 million people in 70 cities.&nbsp;</p>



<p>In 2008, he spearheaded the historic Jena 6 March! During his career he went on a 70 city bus tour to promote mentoring with his One million Mentors Campaign, he was credited by President Obama’s Campaign for being instrumental in his election and reelection, and Michael was the first and loudest national voice to sound the alarm on the Trayvon Martin killing!</p>



<p>Michael is preparing to publish his 8th book as a New York Times Best Selling Author and is the leading voice on promoting Transparent Relationships.&nbsp;</p>



<p>Michael motto is, “Honesty is answering a question truthfully but transparency is volunteering the truth without being asked!”</p>



